package com.example.maven.exercise_3;

public class ConversorDegresRadians {

	public double convertDegresToRadians(double value) {
		double result = Math.toRadians(value);
		return result;
	}

	public double convertRadiansToDegres(double value) {
		double result = Math.toDegrees(value);
		return result;
	}
}